#include "3-calc.h"

/**
 * op_add - prints index of element that satisfied cmp
 * @a: first int
 * @b: second int
 * Return: sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - prints index of element that satisfied cmp
 * @a: first int
 * @b: second int
 * Return: substraction of a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - prints index of element that satisfied cmp
 * @a: first int
 * @b: second int
 * Return: a multiplied by b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - prints index of element that satisfied cmp
 * @a: first int
 * @b: second int
 * Return: division of a and b
 */

int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - prints index of element that satisfied cmp
 * @a: first int
 * @b: second int
 * Return: modulo of a and b
 */
int op_mod(int a, int b)
{
	return (a % b);
}

